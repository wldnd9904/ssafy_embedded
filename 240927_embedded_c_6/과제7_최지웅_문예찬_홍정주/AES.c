void zero_pad(uint32_t input, uint8_t* output) {
    // input: 4바이트 데이터 (uint32_t)
    // output: 16바이트 배열 (AES 블록 크기)

    // 1. 입력 데이터를 output 배열의 처음 4바이트에 복사
    memcpy(output, &input, sizeof(uint32_t));

    // 2. 나머지 12바이트를 제로 패딩 (0x00)으로 채움
    memset(output + sizeof(uint32_t), 0x00, 16 - sizeof(uint32_t));
}


// AES 암호화 함수
void AES_Encrypt(uint32_t input, uint8_t* output) {
    uint8_t input2[16];
    zero_pad(input, input2);
    uint8_t state[AES_BLOCK_SIZE];
    uint8_t roundKeys[AES_BLOCK_SIZE * 11];

    // 입력 데이터를 state로 복사
    memcpy(state, input2, AES_BLOCK_SIZE);

    // 키 확장
    KeyExpansion(key, roundKeys);

    // 초기 라운드에 AddRoundKey 적용
    AddRoundKey(state, roundKeys);

    // 9 라운드 반복
    for (int round = 1; round < 10; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, roundKeys + round * AES_BLOCK_SIZE);
    }

    // 마지막 라운드 (MixColumns 제외)
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, roundKeys + 10 * AES_BLOCK_SIZE);

    // 암호화된 데이터를 출력 버퍼로 복사
    memcpy(output, state, AES_BLOCK_SIZE);


}

// AES 복호화 함수
void AES_Decrypt(uint8_t* input, uint32_t* output) {
    uint8_t state[AES_BLOCK_SIZE];
    uint8_t roundKeys[AES_BLOCK_SIZE * 11];


    // 입력 데이터를 state로 복사
    memcpy(state, input, AES_BLOCK_SIZE);

    // 키 확장
    KeyExpansion(key, roundKeys);

    // 마지막 라운드에 AddRoundKey 적용
    AddRoundKey(state, roundKeys + 10 * AES_BLOCK_SIZE);

    // 마지막 라운드 역연산
    InvShiftRows(state);
    InvSubBytes(state);

    // 9 라운드 반복
    for (int round = 9; round > 0; round--) {
        AddRoundKey(state, roundKeys + round * AES_BLOCK_SIZE);
        InvMixColumns(state);
        InvShiftRows(state);
        InvSubBytes(state);
    }

    // 초기 라운드에 AddRoundKey 적용
    AddRoundKey(state, roundKeys);

    // 복호화된 데이터를 출력 버퍼로 복사
    memcpy(output, state, sizeof(uint32_t));
}



