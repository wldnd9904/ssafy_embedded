// AES Rcon
static const uint8_t Rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
};

// AES Key schedule (키 확장)
void KeyExpansion(uint8_t* key, uint8_t* roundKeys) {
    uint32_t i, j;
    uint8_t temp[4];

    // 첫 번째 라운드 키는 원본 키로 설정
    memcpy(roundKeys, key, AES_BLOCK_SIZE);

    // 10개의 라운드 키 생성
    for (i = AES_BLOCK_SIZE; i < AES_BLOCK_SIZE * 11; i += 4) {
        memcpy(temp, &roundKeys[i - 4], 4);

        if (i % AES_BLOCK_SIZE == 0) {
            // 바이트 순환 및 S-box 변환
            uint8_t t = temp[0];
            temp[0] = sbox[temp[1]] ^ Rcon[i / AES_BLOCK_SIZE];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
        }

        for (j = 0; j < 4; j++) {
            roundKeys[i + j] = roundKeys[i - AES_BLOCK_SIZE + j] ^ temp[j];
        }
    }
}

// AddRoundKey 함수
void AddRoundKey(uint8_t* state, uint8_t* roundKey) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] ^= roundKey[i];
    }
}

// SubBytes 함수
void SubBytes(uint8_t* state) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] = sbox[state[i]];
    }
}

// 역 SubBytes 함수
void InvSubBytes(uint8_t* state) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        state[i] = rsbox[state[i]];
    }
}

